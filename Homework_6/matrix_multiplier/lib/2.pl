#!/usr/bin/perl

use DDP;



my $matrix_a = [
[ 1, 2, 3, 4, 5, 6],
[11,12,13,14,15,16],
[21,22,23,24,25,26],
[31,32,33,34,35,36],
[41,42,43,44,45,46],
[51,52,53,54,55,56],
];

my $matrix_b = [
[ 1, 2, 3, 4, 5, 6],
[11,12,13,14,15,16],
[21,22,23,24,25,26],
[31,32,33,34,35,36],
[41,42,43,44,45,46],
[51,52,53,54,55,56],
];

$x = scalar @$matrix_a - 1;

for my $ind_a (0..$x) {
    for my $ind_b (0..$x) {
        $sum = 0;
        $sum += $matrix_a->[$ind_a][$_] * $matrix_b->[$_][$ind_b] for 0..$x;
        $c->[$ind_a][$ind_b] = $sum;
    }
}

p $c
